int add(int a, int b){
    while(b != 0){
        // –û–±—á–∏—Å–ª—é—î–º–æ –±—ñ—Ç–æ–≤–∏–π –ø–µ—Ä–µ–Ω–æ—Å
        int carry = a & b;
        // –û–±—á–∏—Å–ª—é—î–º–æ —Å—É–º—É –±–µ–∑ –ø–µ—Ä–µ–Ω–æ—Å—É
        a = a ^ b;
        // –ó—Å—É–≤–∞—î–º–æ –ø–µ—Ä–µ–Ω–æ—Å –Ω–∞ –æ–¥–∏–Ω –±—ñ—Ç –≤–ª—ñ–≤–æ
        b = carry << 1;
    }
    return a;
}

