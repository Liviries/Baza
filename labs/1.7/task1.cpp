#include <iostream>

// Функція для демонстрації блочної структури програм
void demonstrateBlocks() {
    // Початок головного блоку
    {
        // Оголошення локальних змінних у головному блоку
        int a = 1;
        int b = 2;
        std::cout << "Головний блок: a = " << a << ", b = " << b << std::endl;

        // Перший вкладений блок
        {
            // Оголошення змінної з ім'ям 'a', демонструючи мембранний ефект
            int a = 10;
            std::cout << "Вкладений блок 1: a = " << a << ", b = " << b << std::endl;
        }

        // Другий вкладений блок
        {
            // Оголошення змінної 'c' у другому вкладеному блоці
            int c = 3;
            std::cout << "Вкладений блок 2: a = " << a << ", b = " << b << ", c = " << c << std::endl;

            // Третій рівень вкладеного блоку
            {
                // Оголошення змінної 'd' у третьому вкладеному блоці
                int d = 4;
                std::cout << "Вкладений блок 3: a = " << a << ", b = " << b << ", c = " << c << ", d = " << d << std::endl;
            }

            // Після виходу з третього блоку
            std::cout << "Після вкладеного блоку 3: a = " << a << ", b = " << b << ", c = " << c << std::endl;
        }

        // Після виходу з другого блоку
        std::cout << "Після вкладеного блоку 2: a = " << a << ", b = " << b << std::endl;
    }
    // Кінець головного блоку
}

int main() {
    std::cout << "Демонстрація блочної структури програм\n" << std::endl;

    // Виклик функції для демонстрації блоків

    demonstrateBlocks();

    return 0;
}